	/*
	 * 转换输入的EBCDIC字符串in_EBC_str,为输出的ASCII字符串out_ASC_str,
	 * 输入的EBCDIC字符串的长度为EBC_str_len
	 */

	/** LFQ unsigned byte *in_EBC_str; 输入的EBCDIC码字符串 * */
	/** LFQ unsigned byte *out_ASC_str; 码制转换后输出的ASCII码字符串 * */
	/** LFQ int EBC_str_len; 输入的EBCDIC码字符串的长度 * */
	public static byte[] pub_base_EBCtoASC(byte[] in_EBC_str, int EBC_str_len) throws Exception{
//		byte[] out_ASC_str = new byte[51920];
		byte[] out_ASC_str = new byte[EBC_str_len*5];
		int in_EBC_str_position, out_ASC_str_position = 0;
		byte EBC_byte_value;
		int EBC_byte_value_int;
		byte[] temp = new byte[2];
		int EBC_PRC_FLAG = 0;

		/* 转换输入的EBCDIC字符串 */
		for (in_EBC_str_position = 0; in_EBC_str_position < EBC_str_len; in_EBC_str_position++) {
			EBC_byte_value = in_EBC_str[in_EBC_str_position] ;
			EBC_byte_value_int = (int)EBC_byte_value & 0xFF;
			
			/* 转换输入的EBCDIC汉字双字节字符 */
			if (EBC_CHINESE_FLAG_START(EBC_byte_value)) {
				EBC_byte_value = in_EBC_str[++in_EBC_str_position];
				EBC_byte_value_int = (int)EBC_byte_value & 0xFF;
				while ((in_EBC_str_position < EBC_str_len) && !(EBC_CHINESE_FLAG_STOP(EBC_byte_value))) {
					temp[0] = EBC_byte_value;
					temp[1] = in_EBC_str[++in_EBC_str_position];
					int temp0_int =(int)(temp[0] & 0xFF);
					int temp1_int =(int)(temp[1] & 0xFF);
					
					if (temp0_int >= (0x40&0xff) && temp0_int <= (0x46&0xff)
							&& temp1_int >= (0x40&0xff) && temp1_int <= (0xfd&0xff)) {
						EBC_PRC_FLAG = 1;
					}
					if ((temp0_int == (0x48&0xff) && temp1_int >= (0xa0&0xff) && temp1_int < (0xfd&0xff))
							|| (temp0_int > (0x48&0xff) && temp0_int < (0x6c&0xff) && temp1_int >= (0x40&0xff) && temp1_int <= (0xfd&0xff))
							|| (temp0_int == (0x6c&0xff) && temp1_int >= (0x40&0xff) && temp1_int <= (0x9f&0xff))) {
						EBC_PRC_FLAG = 2;
					}
					if ((temp0_int >= (0x81&0xff) && temp0_int < (0xa1&0xff) && temp1_int >= (0x40&0xff) && temp1_int <= (0xfd&0xff))
							|| (temp0_int == (0xa1&0xff) && temp1_int >= (0x40&0xff) && temp1_int <= (0x81&0xff))) {
						EBC_PRC_FLAG = 3;
					}
					if ((temp0_int == (0xa1&0xff) && temp1_int >= (0x82&0xff) && temp1_int <= (0xfd&0xff))
							|| (temp0_int > (0xa1&0xff) && temp0_int < (0xcc&0xff)
									&& temp1_int >= (0x40&0xff) && temp1_int <= (0xfd&0xff))
							|| (temp0_int == (0xcc&0xff)&& temp1_int >= (0x40&0xff) && temp1_int <= (0x67&0xff))) {
						EBC_PRC_FLAG = 4;
					}
					if (temp0_int == (0xce&0xff) && temp1_int >= (0x40&0xff) && temp1_int < (0xa6&0xff)) {
						EBC_PRC_FLAG = 5;
					}
					if (temp0_int == (0xcd&0xff) && temp1_int >= (0x40&0xff) && temp1_int <= (0xd5&0xff)) {
						EBC_PRC_FLAG = 6;
					}
					switch (EBC_PRC_FLAG) {
					case 1:
						/* 转换输入的EBCDIC汉字双字节符号,区位从1到9 GB2312 */
						temp = DB_EBCDICtoASCII_01(temp);
						if (temp[0] == 0x00 && temp[1] == 0x00) {
							temp[0] = EBC_byte_value;
							temp[1] = in_EBC_str[in_EBC_str_position];
							temp = DB_EBCDICtoASCII_06(temp);
						}
						break;
					case 2:
						/* 转换输入的EBCDIC汉字双字节字符,区位从16到87 GB2312 */
						temp = DB_EBCDICtoASCII_02(temp);
						break;
					case 3:
						/* 转换输入的EBCDIC汉字双字节字符,GBK3 */
						temp = DB_EBCDICtoASCII_03(temp);
						break;
					case 4:
						/* 转换输入的EBCDIC汉字双字节字符, GBK4 */
						temp = DB_EBCDICtoASCII_04(temp);
						break;
					case 5:
						/* 转换输入的EBCDIC汉字双字节字符,GBK5 */
						temp = DB_EBCDICtoASCII_05(temp);
						break;
					case 6:
						/* 转换输入的EBCDIC汉字双字节符号,GBK6 */
						temp = DB_EBCDICtoASCII_06(temp);
						break;

					default:
						break;
					}
					/**
					 * LFQ printf("ASC is %x_%x
					 * %d\n",temp.first_byte,temp.second_byte,EBC_PRC_FLAG); *
					 */
					out_ASC_str[out_ASC_str_position++] = temp[0];
					out_ASC_str[out_ASC_str_position++] = temp[1];
					
					EBC_byte_value = in_EBC_str[++in_EBC_str_position];
					EBC_byte_value_int = (int)EBC_byte_value & 0xFF;
				}
				out_ASC_str[out_ASC_str_position++] = (byte)(' ');
				out_ASC_str[out_ASC_str_position++] = (byte)(' ');
				
			}
			/* 转换输入的EBCDIC英文单字节字符 */
			else
				out_ASC_str[out_ASC_str_position++] = SB_EBCDICtoASCII(EBC_byte_value_int);
		}
		int j=0;
		for (int i=0;i<out_ASC_str.length;i++){
			if (out_ASC_str[i] != 0x00){
				j++;
				
			}		
		}
		byte[] out = new byte[j];
		System.arraycopy(out_ASC_str, 0, out, 0, j);
		return out;
	}

	/*
	 * 转换输入的ASCII字符串in_ASC_str,为输出的EBCDIC字符串out_EBC_str,
	 * 输入的ASCII字符串的长度为ASC_str_len
	 */
	/** LFQ unsigned byte *in_ASC_str; 输入的ASCII码字符串 * */
	/** LFQ unsigned byte *out_EBC_str; 码制转换后输出的EBCDIC码字符串 * */
	/** LFQ int ASC_str_len; 输入的ASCII码字符串的长度 * */
	public static byte[] pub_base_ASCtoEBC(byte[] in_ASC_str, int ASC_str_len) throws Exception{
//		byte[] out_EBC_str = new byte[51920];		
		byte[] out_EBC_str = new byte[ASC_str_len*4];
		int num_of_space_for_chinese = 0; /* space number */
		int in_ASC_str_position, out_EBC_str_position = 0;
		byte ASC_byte_value;
		int ASC_byte_value_int;
		byte[] temp = new byte[2];
		int ASC_PRC_FLAG = 0;
		/* 转换输入的ASCII字符串 */
		for (in_ASC_str_position = 0; in_ASC_str_position < ASC_str_len; in_ASC_str_position++) {
			ASC_byte_value = in_ASC_str[in_ASC_str_position];
			ASC_byte_value_int = (int)ASC_byte_value & 0xFF;
			/* 转换输入的ASCII汉字符 */
			if (ASC_byte_value_int >= (int)(0x81 & 0xFF)) {
				out_EBC_str[out_EBC_str_position++] = EBC_CHINESE_FLAG_START_byte;
				while ((in_ASC_str_position < ASC_str_len) && (ASC_byte_value_int >= 0x81)) {
					temp[0] = ASC_byte_value; 
					temp[1] = in_ASC_str[++in_ASC_str_position];
					int temp0_int =(int)(temp[0] & 0xFF);
					int temp1_int =(int)(temp[1] & 0xFF);
					
					if ((temp0_int >= (0xa1 & 0xFF) && temp0_int <= (0xa9 & 0xFF) 
							&& temp1_int >= (0xa0 & 0xFF) && temp1_int <= (0xfe & 0xFF))) {
						ASC_PRC_FLAG = 1;
					}
					if ((temp0_int >= (0xb0 & 0xFF) && temp0_int <= (0xf7 & 0xFF)) 
							&& (temp1_int >= (0xa0 & 0xFF) && temp1_int <= (0xfe & 0xFF))) {
						ASC_PRC_FLAG = 2;
					}
					if ((temp0_int >= (0x81 & 0xFF) && temp0_int <= (0xa0 & 0xFF)) 
							&& (temp1_int >= (0x40 & 0xFF) && temp1_int <= (0xfe & 0xFF))) {
						ASC_PRC_FLAG = 3;

					}
					if (((temp0_int >= (0xaa & 0xff) && temp0_int <= (0xfc &0xff)) 
							&& (temp1_int >= (0x40 &0xff) && temp1_int <= (0xa0 &0xff)))
							|| (temp0_int == (0xfd &0xff) && temp1_int >= (0x40 &0xff) && temp1_int < (0x9c &0xff))) {
						ASC_PRC_FLAG = 4;
					}
					if (((temp0_int == (0xfd&0xff) && temp1_int >= (0x9c&0xff) && temp1_int <= (0xa0&0xff))) 
							|| (temp0_int == (0xfe&0xff) && temp1_int >= (0x40&0xff) && temp1_int <= (0xa0&0xff)))
					{
						ASC_PRC_FLAG = 5;
					}
					if ((temp0_int >= (0xa8&0xff) && temp0_int <= (0xa9&0xff)) 
							&& (temp1_int >= (0x40&0xff) && temp1_int <= (0x96&0xff))) {
						ASC_PRC_FLAG = 6;
					}
					switch (ASC_PRC_FLAG) {
					case 1:
						/* 转换输入的GB2312 ASCII汉字符号,区位从1到9 */
						temp = DB_ASCIItoEBCDIC_01(temp);
						break;
					case 2:
						/* 转换输入的GB2312 ASCII汉字字符 */
						temp = DB_ASCIItoEBCDIC_02(temp);
						break;
					case 3:
						/* 转换输入的GBK ASCII汉字字符 0x81-0xA0 */
						temp = DB_ASCIItoEBCDIC_03(temp);
						break;
					case 4:
						/* 转换输入的GBK ASCII汉字字符 0xaa-0xfe */
						temp = DB_ASCIItoEBCDIC_04(temp);
						break;
					case 5:
						/* 转换输入的GBK ASCII汉字字符 0xaa-0xfe */
						temp = DB_ASCIItoEBCDIC_05(temp);
						break;
					case 6:
						/* 转换输入的GBK ASCII汉字符号 0x40-0x96 */
						temp = DB_ASCIItoEBCDIC_06(temp);
						break;
					default:
						break;

					}
					/**
					 * LFQ printf("EBC is %x_%x
					 * %d\n",temp[0],temp[1],ASC_PRC_FLAG); *
					 */
					out_EBC_str[out_EBC_str_position++] = temp[0];
					out_EBC_str[out_EBC_str_position++] = temp[1];
					++in_ASC_str_position;
					if (in_ASC_str_position < ASC_str_len) {
						ASC_byte_value = in_ASC_str[in_ASC_str_position];
						ASC_byte_value_int = (int)ASC_byte_value & 0xFF;
					}
				}
				out_EBC_str[out_EBC_str_position++] = EBC_CHINESE_FLAG_STOP_byte;
				in_ASC_str_position--;
				num_of_space_for_chinese = num_of_space_for_chinese + 2;
			}
			/* 转换输入的ASCII英文单字节字符 */
			else {
				if (ASC_byte_value == SPACE)
					if (num_of_space_for_chinese != 0) {
						num_of_space_for_chinese--;
//						continue;
					}
				
				out_EBC_str[out_EBC_str_position++] = SB_ASCIItoEBCDIC(ASC_byte_value_int);
			}
		}
		int j=0;
		for (int i=0;i<out_EBC_str.length;i++){
			if (out_EBC_str[i] != 0x00){
				j++;
			}		
		}
		byte[] out = new byte[j];
		System.arraycopy(out_EBC_str, 0, out, 0, j);
		return out;
	}
	
