		byte m_index, s_index;
		byte[] temp = new byte[2];
		m_index = (byte) (asc_byte[0] - 0xa1);
		s_index = (byte) (asc_byte[1] - 0xa1);
		temp[0] = IBMtoGBTable[(m_index&0xff)][(s_index&0xff) * 2];
