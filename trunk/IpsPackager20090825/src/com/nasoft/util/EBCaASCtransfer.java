/*******************************************************************************
 * 单字节码表转换遵循IBM InforWindow 3477-S型 * 中文工作站用户指南的码表转换原则。 * 用户如有特殊需求,可自行对码表进行填充。 *
 * 双字节码表转换遵循IBM Multistation 5550 * 汉字编码手册。 * 单字节码表: * ASCII字符转换成EBCDIC码表 *
 * ASCIItoEBCDICTable[256] * EBCDIC字符转换成ASCII码表 * EBCDICtoASCIITable[256] *
 * 双字节码表: * IBM双字节字符转换成国标GB-2313汉字 * 或国标GB-2313汉字转换成IBM双字节字符。 * 仅限于前九区的汉字码表。 *
 * IBMtoGBTable[9][188] * 由于十六区至八十七区汉字转换有规律 * 可循,故不需要码表,直接转换即可。 * *
 * ---------------------------------------------* GBK支持: * GBK 规范收录了ISO 10646.1
 * 中的全部 * CJK汉字和符号，并有所补充， * 具体包括: * 1. GB 2312 中的全部汉字、非汉字符号 * 2. GB
 * 13000.1中的其他CJK汉字 * 以上合计20902个GB化汉字 * 3.《简化字总表》中未收录GB 13000.1的 * 52个汉字 *
 * 4.《康熙字典》及《辞海》中未收录 * GB 13000.1的28个部首及重要构件 * 5. 13个汉字结构符 * 6. BIG-5中未被GB
 * 2312收入，但存在于 * GB 13000.1中的139个图形符号 * 7. GB 12345增补的6个拼音符号 * 8. 汉字"○" * 9. GB
 * 12345增补的19个竖排标点符号 * 10.从GB 13000.1的CJK兼容区挑选出的 * 21个汉字 * 11.GB
 * 13000.1收入的31个IBM OS/2专用 * 符号 *
 ******************************************************************************/

