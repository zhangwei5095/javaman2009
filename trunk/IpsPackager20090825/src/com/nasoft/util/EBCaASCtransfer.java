	/* 转换输入的ASCII汉字符,区位从16到87 */
	public static byte[] DB_ASCIItoEBCDIC_02(byte[] asc_byte)throws Exception {
		byte firstbyte = (byte) (asc_byte[0] - ASC_PARTITION_AREA_FLAG_byte);
		byte[] temp = { 0x00, 0x00 };
		if (firstbyte % 2 != 0)
			firstbyte++;
		firstbyte /= 2;
		temp[0] = (byte) (firstbyte + 0x48);
		if (asc_byte[0] % 2 != 0) {
			if ((asc_byte[1]&0xff) < (0xe0&0xff))
				temp[1] = (byte) (asc_byte[1] - 0x60);
			else
				temp[1] = (byte) (asc_byte[1] - 0x5f);
		} else
			temp[1] = (byte) (asc_byte[1] - 1);
		return temp;
	}

