package com.nasoft.fixed;

public class FixedField 
{
	private int key;
	private byte[] value;
	public int getKey() 
	{
		return key;
	}
	public void setKey(int key) 
	{
		this.key = key;
	}
	public byte[] getValue() 
	{
		return value;
	}
	public void setValue(byte[] value) 
	{
		this.value = value;
	}
	
}
